DATA SEGMENT
	BCD_IN DB 89H
	BIN_VAL DB 1 DUP (0)
DATA ENDS

STACK_SEG SEGMENT 
	       DW 100 DUP (0)
	TOP LABEL WORD
STACK_SEG ENDS

CODE SEGMENT
	ASSUME CS:CODE,DS:DATA,SS:STACK_SEG
  START:MOV AX,DATA
	MOV DS,AX
	MOV AX,STACK_SEG
	MOV SS,AX
	LEA SP,TOP
	MOV AL,BCD_IN
	CALL BCD_BIN
	MOV BIN_VAL,AL
	MOV AH,4CH
	INT 21H

	BCD_BIN PROC NEAR
		PUSHF
		MOV BL,AL
		AND BL,0FH
		AND AL,0F0H
		MOV CL,04H
		ROR AL,CL
		MOV BH,0AH
 		MUL BH
		ADD AL,BL
		POPF
		RET	
	BCD_BIN ENDP
	CODE ENDS
     END START	
 